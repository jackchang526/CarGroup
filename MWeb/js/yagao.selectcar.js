var SelectCar = {
    Condition: { "yagao": 0, "price": 0, "stage": 0 },
    GetSerialData: function (fnEnd) {
        var self = this;

        if (self.Condition.yagao == 0 || self.Condition.price == 0 || self.Condition.stage == 0) {
            return;
        }
        $.ajax({
            type: "get",
            url: "/handlers/YaGaoSelectCar.ashx",
            data: { yagao: self.Condition.yagao, price: self.Condition.price, stage: self.Condition.stage },
            dataType:"json",
            success: function (data) {
                if (data) {
                    if (data.msg != "") {
                        console.log(data.msg);
                        return;
                    }
                    var htmlArray = new Array();
                    htmlArray.push("<i class=\"zoomUseOut animated duration3\"></i>");
                    htmlArray.push("<p>" + data.result.recommend + "</p>");
                    if (data.result.serial.length > 0) {
                        var csName = data.result.serial[0].csName;
                        if (csName.length > 9) {
                            csName = csName.substring(0, 9) + "...";
                        }
                        htmlArray.push("<a href=\"http://car.m.yiche.com/" + data.result.serial[0].allSpell + "/\">");
                        htmlArray.push("<div class=\"car-content\">");
                        htmlArray.push("<img src=\"" + data.result.serial[0].imgUrl + "\" />");
                        htmlArray.push("<div class=\"content\">");
                        htmlArray.push("<h5>" + csName + "</h5>");
                        htmlArray.push("<strong class=\"price\">" + data.result.serial[0].referPrice + "</strong>");
                        htmlArray.push("<strong>" + data.result.configure + "</strong>");
                        htmlArray.push("</div>");
                        htmlArray.push("</div>");
                        htmlArray.push("</a>");
                    }
                    if (data.result.serial.length > 1) {
                        htmlArray.push("<ul class=\"car-list\">");
                        for (var i = 1; i < data.result.serial.length; i++) {
                            var csName = data.result.serial[i].csName;
                            if (csName.length > 5) {
                                csName = csName.substring(0, 5) + "...";
                            }
                            htmlArray.push("<li>");
                            htmlArray.push("<a href=\"http://car.m.yiche.com/" + data.result.serial[i].allSpell + "/\">");
                            htmlArray.push("<img src=\"" + data.result.serial[i].imgUrl + "\" />");
                            htmlArray.push("<h5>" + csName + "</h5>");
                            htmlArray.push("<strong>" + data.result.serial[i].referPrice + "</strong>");
                            htmlArray.push("</a>");
                            htmlArray.push("</li>");
                        }
                        htmlArray.push("</ul>");
                    }
                    $(".boardlist").html(htmlArray.join(""));
                    //Bglog_InitPostLog();
                    BglogPostLog(StatisticsJson[self.Condition.yagao][self.Condition.stage][self.Condition.price]);
                    fnEnd && fnEnd();

                }
            },
            error: function (msg) {
                console.log(msg);
            }
        });
    }
}


